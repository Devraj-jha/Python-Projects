num1 = float(input("Enter your Number 1: "))
num2 =  float(input("Enter your Number 2: "))

operator = input("Enter your operator: ")

if operator == "+":
    print(num1 + num2)
elif operator == "-":
    print(num1 - num2)
elif operator == "/":
    print(num1/ num2)
elif operator == "*":
    print(num1 * num2)
